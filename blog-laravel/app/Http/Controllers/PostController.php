    public function store(Request $request)
    {
        $request->validate([
            'title' => 'required',
            'text' => 'required'
        ]);

        Post::create([
            'title' => $request->title,
            'text' => $request->text,
            'slug' => str($request->title)->slug()
        ]);

        return redirect('/posts');
    }

